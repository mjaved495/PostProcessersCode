package edu.cornell.scholars.ospgrants;

public class Person_NetIdDeptMap {

	private String personURI;
	private String netId;
	private String dept;
	private String deptURI;
	private String college;
	
	
	public String getPersonURI() {
		return personURI;
	}
	public void setPersonURI(String personURI) {
		this.personURI = personURI;
	}
	public String getNetId() {
		return netId;
	}
	public void setNetId(String netId) {
		this.netId = netId;
	}
	public String getDept() {
		return dept;
	}
	public void setDept(String dept) {
		this.dept = dept;
	}
	public String getDeptURI() {
		return deptURI;
	}
	public void setDeptURI(String deptURI) {
		this.deptURI = deptURI;
	}
	public String getCollege() {
		return college;
	}
	public void setCollege(String college) {
		this.college = college;
	}
	
}
